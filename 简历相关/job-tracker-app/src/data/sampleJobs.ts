function createSampleJob(input: SampleJobInput): Job {
  const salary = parseSalaryText(input.salaryText);
  const stageTier = getStageTier(input.companyStage);
  const jdRaw = getRawJobDescription(input.sourceIndex) ?? input.jdRaw;

  return {
    id: `job-${String(input.sourceIndex).padStart(3, "0")}`,
    sourceIndex: input.sourceIndex,
    companyName: input.companyName,
    companyStage: input.companyStage,
    jobTitle: input.jobTitle,
    salaryText: input.salaryText,
    currentSalaryK: 18,
    ...salary,
    jdRaw,
    responsibilities: input.responsibilities ?? [],
    requirements: input.requirements ?? [],
    bonusItems: input.bonusItems ?? [],
    techStack: input.techStack ?? [],
    businessTags: input.businessTags ?? [],
    riskTags: input.riskTags ?? [],
    stageTier,
    fitTier: input.fitTier ?? stageTier,
    userIntent: input.userIntent ?? "normal",
    matchScore: input.matchScore ?? 60,
    priority: input.priority ?? "P2",
    applicationStatus: "pending_apply",
    greetingStatus: "not_generated",
    fitReason: input.fitReason,
    riskReason: input.riskReason,
    prepNotes: input.prepNotes ?? [],
    interviewQuestions: input.interviewQuestions ?? [],
    createdAt: now,
    updatedAt: now,
    remark: input.remark,
  };
}

function getRawJobDescription(sourceIndex: number): string | undefined {
  const pattern = new RegExp(
    `(?:^|\\n)(--${sourceIndex}(?:\\s|$)[\\s\\S]*?)(?=\\n--\\d+(?:\\s|$)|$)`,
  );
  const match = jobsSourceText.match(pattern);
  const rawBlock = match?.[1]?.trim();
  if (!rawBlock) return undefined;

  const [, , ...descriptionLines] = rawBlock.split(/\r?\n/);
  return descriptionLines.join("\n").trim();
}

